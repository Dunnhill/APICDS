function H = H_FUNCTION(X)
% ‘ункци€ вычислени€ правой части модели измерений
% ’ - вектор состо€ний
% Y - вектор измерений без шума
global m
H=zeros(size(X,1),m);
% .*(180/pi) - перевод из радиан в градусы
H(:,1) = atan(X(:,2)./X(:,1)).*(180/pi);
H(:,2) = X(:,4).*(180/pi);
H(:,3) = sqrt(X(:,1).^2+X(:,2).^2);
H(:,4) = X(:,3).*(180/pi);
end